<?php

declare(strict_types=1);

namespace App\MC\Entity;

use App\Tenant\Entity\TenantEntity;
use Doctrine\Common\Collections\ArrayCollection;
use Doctrine\Common\Collections\Collection;
use Doctrine\ORM\Mapping as ORM;
use Ramsey\Uuid\UuidInterface;

/**
 * @ORM\Entity
 */
class McLine extends TenantEntity
{
    /**
     * @ORM\Id
     * @ORM\Column
     */
    public UuidInterface $id;

    /**
     * @ORM\ManyToOne(targetEntity=McEquipment::class, inversedBy="lines")
     */
    public McEquipment $equipment;

    /**
     * @ORM\ManyToOne(targetEntity=McWork::class, fetch="EAGER")
     */
    public McWork $work;

    /**
     * @var Collection<int, McPart>
     *
     * @ORM\OneToMany(targetEntity=McPart::class, mappedBy="line")
     */
    public iterable $parts;

    /**
     * @ORM\Column(type="integer")
     */
    public int $period;

    /**
     * @ORM\Column(type="boolean")
     */
    public bool $recommended = false;

    /**
     * @ORM\Column(type="integer")
     */
    public int $position;

    public function __construct(
        UuidInterface $id,
        McEquipment $equipment,
        McWork $work,
        int $period,
        bool $recommended,
        int $position = 0,
    ) {
        $this->id = $id;
        $this->equipment = $equipment;
        $this->work = $work;
        $this->parts = new ArrayCollection();
        $this->period = $period;
        $this->recommended = $recommended;
        $this->position = $position;
    }

    public function toId(): UuidInterface
    {
        return $this->id;
    }
}
